import turtle

tim = turtle.Turtle()  # -creating a turtle
tim.color('red')  # -setting the color of a turtle
tim.pensize(5)  # -changing the pen size
tim.shape('turtle')  # -more shapes on google
# moves for turtle:
tim.forward(190)
tim.backward(100)
tim.left(130)  # - at an angle
tim.right(100)

tim.forward(90)
tim.right(100)
tim.left(90)
tim.backward(100)
tim.color('green')
tim.forward(100)
tim.penup()
tim.forward(89)
tim.pendown()

