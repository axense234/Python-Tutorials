
# .strip() -removes the blank space from a string
# len() -gives the length of a string
# .lower() -converts the string into lower cases
# .upper() -converts the string into upper cases
# .split() -splits the string into multiple strings,delimiting them by whats in the ()
text = input("Input something: ")
print(text.strip())
print(len(text))
print(text.lower())
print(text.upper())
print(text.split())
